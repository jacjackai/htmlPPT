/**
 * Get all theme presets
 * @returns {Array<Object>} All theme presets
 */
export function getAllThemes() {
  return [
    lightTheme,
    darkTheme,
    businessTheme,
    creativeTheme,
    minimalTheme,
    oceanTheme,
    forestTheme,
    sunsetTheme,
  ];
}

/**
 * Get theme by name
 * @param {string} name - Theme name
 * @returns {Object|null} Theme or null
 */
export function getThemeByName(name) {
  const themes = getAllThemes();
  return themes.find((theme) => theme.name === name) || null;
}

/**
 * Get theme names
 * @returns {Array<string>} Theme names
 */
export function getThemeNames() {
  return getAllThemes().map((theme) => theme.name);
}
